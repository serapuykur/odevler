package SlaytOdevleri3;

public class ForloopSoru3 {
    public static void main(String[] args) {

        /*soru 3= 100 den baslayarak 50 ye kadar (50 dahil)kadar olan sayilari
         aralarinda virgul olarak ayni  satirda yazdirin

         */

        for (int i =100; i >=50; i--) {
           if(i>50){
               System.out.print(i+",");
           }else System.out.print(i);

        }

    }
}
